		<td>
			4.	Anda dapat mengubah status pengajuan menjadi <b>diterima</b> jika pengajuan yang diajukan <b>sama</b> dengan jadwal kegiatan yang dimaksud dalam pengajuan tersebut. &nbsp;&nbsp;&nbsp;&nbsp;Harap isi alasan Anda menerima pengajuan tersebut.
		</td>
	</tr>
	<tr>
		<td>
			5.	Anda dapat mengubah status pengajuan menjadi <b>ditolak</b> jika pengajuan yang diajukan <b>tidak sama</b> dengan jadwal kegiatan yang dimaksud dalam pengajuan &nbsp;&nbsp;&nbsp;&nbsp;tersebut. Harap isi alasan Anda menolaknya dan harap memberikan jangka waktu untuk pelaksana kegiatan agar diperbaiki pengajuannya.
		</td>
	</tr>
	<tr>
		<td>
			6.	Setelah status pengajuan Anda telah menggantinya dengan status "diterima" maka otomatis pengajuan tersebut akan masuk ke dalam sistem Bendahara, maka &nbsp;&nbsp;&nbsp;&nbsp;Anda harus menunggu pengajuan tersebut disetujui atau tidak oleh Bendahara. Jika pengajuan tersebut belum ada respon sampai <b>24 jam</b> maka Anda dapat &nbsp;&nbsp;&nbsp;&nbsp;langsung menemui Bendahara. Anda dapat langsung menemui Bendahara jika pengajuan tersebut sudah <b>disetujui</b> oleh Bendahara beserta meminta anggaran yang &nbsp;&nbsp;&nbsp;&nbsp;diminta di dalam pengajuan tersebut.
