			2.	Pada menu Pengajuan terdapat 4 sub menu, yaitu sub menu pengajuan yang masuk, diterima, ditolak , dan disetujui. Klik pada sub menu "Pengajuan Masuk", jika &nbsp;&nbsp;&nbsp;&nbsp;Anda ingin melihat pengajuan yang masuk ke dalam sistem Anda, Klik pada sub menu "Pengajuan Diterima" jika Anda ingin melihat pengajuan yang telah Anda &nbsp;&nbsp;&nbsp;&nbsp;terima, Klik pada sub menu "Pengajuan Ditolak" jika Anda ingin melihat pengajuan yang telah Anda tolak, dan Klik pada sub menu "Pengajuan Disetujui" jika &nbsp;&nbsp;&nbsp;&nbsp;Anda ingin melihat pengajuan yang telah disetujui oleh Bendahara.		</td>
