<h2>Tata Cara Umum Penggunaan Aplikasi Monitoring Anggaran Satuan Kerja</h2>
<hr>
<body>
<p align="left"><b>Berikut Tata Caranya &nbsp; :</b></p>
<br>
<table>
	<tr>
		<td>
			1.	Di halaman antar muka Anda terdapat 6 menu utama, yaitu : menu halaman utama, menu kegiatan, pengajuan, pelaporan, ubah informasi, dan cetak.
		</td>
	</tr>
	<tr>
		<td>
			2.	Arahkan Pointer ke menu "Kegiatan" maka akan muncul sub - sub menu dari menu kegiatan, Anda dapat melihat menu "Lihat Kegiatan", menu "Lihat Realisasi &nbsp;&nbsp;&nbsp;&nbsp;Kegiatan", menu "Sedang Dilaksanakan", dan menu "Sudah Dilaksanakan".
		</td>
	</tr>
	<tr>
		<td>
			3.	Klik pada menu "Lihat Kegiatan" maka Anda akan melihat seluruh jadwal kegiatan yang belum dilaksanakan oleh pegawai (pelaksana kegiatan) dari subbidang - &nbsp;&nbsp;&nbsp;&nbsp;subbidang yang Anda tanggungjawabkan. Anda dapat mengubah status kegiatan di dalam jadwal kegiatan tersebut jika pelaksana kegiatan telah membuat &nbsp;&nbsp;&nbsp;&nbsp;pengajuan yang berkaitan dengan jadwal kegiatan yang akan dilaksanakan.
		</td>
	</tr>
	<tr>
		<td>
			4.	Klik pada menu "Lihat Realisasi Kegiatan" maka Anda akan melihat seluruh riwayat realisasi kegiatan yang Anda buat, Anda dapat menambahkan data realisasi &nbsp;&nbsp;&nbsp;&nbsp;kegiatan yang baru dengan klik tombol "TAMBAH" dengan catatan data realisasi harus sesuai dengan kode pengajuan, kode pelaporan, dan kode kegiatan yang &nbsp;&nbsp;&nbsp;&nbsp;berkaitan dengan realisasi tersebut.
		</td>
	</tr>
	<tr>
		<td>
			5.	Klik pada menu "Sedang Dilaksanakan" maka Anda akan melihat seluruh jadwal kegiatan yang ada di dalam bidang Anda dengan status sedang dilaksanakan. &nbsp;&nbsp;&nbsp;&nbsp;Anda dapat mengubah status kegiatan tersebut jika pelaksana kegiatan telah membuat pelaporan dari kegiatannya tersebut.
		</td>
	</tr>
	<tr>
		<td>
			6.	Klik pada menu "Sudah Dilaksanakan" maka Anda akan melihat seluruh jadwal kegiatan yang ada di dalam bidang Anda dengan status sudah dilaksanakan.
		</td>
	</tr>
	<tr>
		<td>
			7.	Arahkan pointer pada menu "Pengajuan" maka akan muncul sub - sub menu dari menu pengajuan, Anda dapat melihat menu "Pengajuan Diterima", dan menu &nbsp;&nbsp;&nbsp;&nbsp;"Pengajuan Ditolak". Untuk lebih jelasnya Anda dapat melihat ini di dalam "tata cara pengajuan".
		</td>
	</tr>
	<tr>
		<td>
			8.	Arahkan pointer pada menu "Pelaporan" maka akan muncul sub - sub menu dari menu pelaporan, Anda dapat melihat menu "Pelaporan Diterima", dan menu &nbsp;&nbsp;&nbsp;&nbsp;"Pengajuan Ditolak". Untuk lebih jelasnya Anda dapat melihat ini di dalam "tata cara pelaporan".
		</td>
	</tr>
	<tr>
		<td>
			9.	Arahkan pointer pada menu "Ubah Informasi" maka akan muncul sub - sub menu dari menu ubah informasi, Anda dapat melihat menu "Ubah Identitas" dan menu &nbsp;&nbsp;&nbsp;&nbsp;"Ubah Kata Sandi".
		</td>
	</tr>
	<tr>
		<td>
			10.	Jika Anda ingin mengubah Informasi tentang Anda, Anda dapat mengklik menu "Ubah Identitas". Di dalam menu tersebut terdapat NIP, Nama, Tanggal Lahir, &nbsp;&nbsp;&nbsp;&nbsp;Telepon, dan Alamat mengenai Anda. Anda hanya dapat mengubah Nama,Tanggal Lahir , Telepon , dan Alamat. Ketika Anda ingin menyimpan perubahannya, &nbsp;&nbsp;&nbsp;&nbsp;Anda dapat mengklik tombol "Simpan" jika membatalkannya Anda dapat mengklik tombol "Batal".
		</td>
	</tr>
	<tr>
		<td>
			11.	Jika Anda ingin mengubah kata sandi Anda , Anda dapat mengklik menu "Ubah Kata Sandi". Di dalam menu tersebut terdapat inputan kata sandi lama Anda, dan &nbsp;&nbsp;&nbsp;&nbsp;kata sandi yang akan diperbaharui. Anda harus mengisi kata sandi lama anda setelah itu mengisi kata sandi baru Anda. Ketika Anda ingin menyimpan &nbsp;&nbsp;&nbsp;&nbsp;perubahannya, Anda dapat mengklik tombol "Simpan" jika membatalkannya Anda dapat mengklik tombol "Batal".
		</td>
	</tr>
	<tr>
		<td>
			12.	Arahkan pointer pada menu "Cetak" maka akan muncul sub - sub menu dari menu cetak, Anda dapat melihat menu "Data Kegiatan", dan "Data Realisasi"
		</td>
	</tr>
	<tr>
		<td>
			13.	Anda dapat mencetak data kegiatan Anda di dalam aplikasi monitoring anggaran ini dengan mengklik Sub menu "Data Kegiatan" yang terdapat di dalam menu &nbsp;&nbsp;&nbsp;&nbsp;"Cetak". Anda dapat memilih waktu tanggal kegiatan Anda, memilih jadwal kegiatan berdasarkan subbidang, dan status kegiatan Anda yang akan Anda Cetak. &nbsp;&nbsp;&nbsp;&nbsp;Setelah itu Anda Klik tombol "Kirim", maka Anda dapat melihat seluruh data kegiatan sesuai dengan pilihan Anda. Jika ingin mencetaknya Anda dapat mengklik &nbsp;&nbsp;&nbsp;&nbsp;tombol "Cetak".
		</td>
	</tr>
	<tr>
		<td>
			14.	Anda dapat mencetak data realisasi Anda di dalam aplikasi monitoring anggaran ini dengan mengklik Sub menu "Data Realisasi" yang terdapat di dalam menu &nbsp;&nbsp;&nbsp;&nbsp;"Cetak". Anda dapat memilih waktu tanggal realisasi yang telah Anda buat. Setelah itu Anda Klik tombol "Kirim", maka Anda dapat melihat seluruh data &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;realisasi sesuai dengan pilihan Anda. Jika ingin mencetaknya Anda dapat mengklik tombol "Cetak".
		</td>
	</tr>
