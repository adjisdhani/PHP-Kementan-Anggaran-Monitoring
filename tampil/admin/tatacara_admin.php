		</td>
	</tr>
	<tr>
		<td>
			2.	Klik pada menu "Pengguna" jika Anda ingin melihat data pengguna dari aplikasi monitoring anggaran ini. Dan Anda juga dapat menambahkan data pengguna &nbsp;&nbsp;&nbsp;&nbsp;dengan mengklik tombol "tambah" .
		</td>
	</tr>
	<tr>
		<td>
			3.	Arahkan pointer pada menu "Kegiatan" maka akan muncul sub - sub menu dari menu kegiatan, Anda dapat melihat menu "Judul Kegiatan", menu "Sub Judul &nbsp;&nbsp;&nbsp;&nbsp;Kegiatan", dan menu "Lihat Kegiatan". 
		</td>
	</tr>
	<tr>
		<td>
			4.	Jika Anda ingin melihat data judul kegiatan maka anda dapat klik pada menu "Judul Kegiatan" dan Anda dapat menambahkan data judul kegiatan dengan mengklik &nbsp;&nbsp;&nbsp;&nbsp;tombol "tambah".
		</td>
	</tr>
	<tr>
		<td>
			5.	Jika Anda ingin melihat data sub judul kegiatan maka anda dapat klik pada menu "Sub Judul Kegiatan" dan Anda dapat menambahkan data sub judul kegiatan &nbsp;&nbsp;&nbsp;&nbsp;dengan mengklik tombol "tambah".
		</td>
	</tr>
	<tr>
		<td>
			6.	Jika Anda ingin melihat data kegiatan maka anda dapat klik pada menu "Kegiatan" dan Anda dapat menambahkan data kegiatan dengan mengklik tombol &nbsp;&nbsp;&nbsp;&nbsp;"tambah".
		</td>
	</tr>
	<tr>
		<td>
			7.	Arahkan pointer pada menu "Ubah Informasi" maka akan muncul sub - sub menu dari menu ubah informasi, Anda dapat melihat menu "Ubah Identitas" dan menu &nbsp;&nbsp;&nbsp;&nbsp;"Ubah Kata Sandi".
		</td>
	</tr>
	<tr>
		<td>
			8.	Jika Anda ingin mengubah Informasi tentang Anda, Anda dapat mengklik menu "Ubah Identitas". Di dalam menu tersebut terdapat NIP, Nama, Tanggal Lahir, &nbsp;&nbsp;&nbsp;&nbsp;Telepon, dan Alamat mengenai Anda. Anda hanya dapat mengubah Nama,Tanggal Lahir , Telepon , dan Alamat. Ketika Anda ingin menyimpan perubahannya, &nbsp;&nbsp;&nbsp;&nbsp;Anda dapat mengklik tombol "Simpan" jika membatalkannya Anda dapat mengklik tombol "Batal".
		</td>
	</tr>
	<tr>
		<td>
			9.	Jika Anda ingin mengubah kata sandi Anda , Anda dapat mengklik menu "Ubah Kata Sandi". Di dalam menu tersebut terdapat inputan kata sandi lama Anda, dan &nbsp;&nbsp;&nbsp;&nbsp;kata sandi yang akan diperbaharui. Anda harus mengisi kata sandi lama anda setelah itu mengisi kata sandi baru Anda. Ketika Anda ingin menyimpan &nbsp;&nbsp;&nbsp;&nbsp;perubahannya, Anda dapat mengklik tombol "Simpan" jika membatalkannya Anda dapat mengklik tombol "Batal".
		</td>
	</tr>
	<tr>
		<td>
			10.	Arahkan pointer pada menu "Cetak" maka akan muncul sub menu dari menu ubah informasi, Anda dapat melihat menu "Data Pengguna".
		</td>
	</tr>
	<tr>
		<td>
			11.	Anda dapat mencetak data pengguna aplikasi monitoring anggaran ini dengan mengklik Sub menu "Data Pengguna" yang terdapat dalam menu "Cetak". Anda &nbsp;&nbsp;&nbsp;&nbsp;dapat memilih bidang , sub bidang, dan status pengguna yang akan Anda Cetak. Setelah itu Anda Klik tombol "Kirim", maka Anda dapat melihat seluruh data &nbsp;&nbsp;&nbsp;&nbsp;pengguna sesuai dengan pilihan Anda. Jika ingin mencetaknya Anda dapat mengklik tombol "Cetak" .
		</td>
