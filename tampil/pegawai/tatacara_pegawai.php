<p align="left"><b>Berikut Tata Caranya &nbsp; :</b></p>
<br>
<table>
	<tr>
		<td>
			1.	Di halaman antar muka Anda terdapat 6 menu utama, yaitu : menu halaman utama, menu kegiatan, pengajuan, pelaporan, ubah informasi, dan cetak.
		</td>
	</tr>
	<tr>
		<td>
			2.	Arahkan Pointer ke menu "Kegiatan", jika Anda ingin melihat jadwal kegiatan Anda .
		</td>
	</tr>
	<tr>
		<td>
			3.	Arahkan pointer pada menu "Pengajuan" maka akan muncul sub - sub menu dari menu pengajuan, Anda dapat melihat menu "Pengajuan Diterima", dan menu &nbsp;&nbsp;&nbsp;&nbsp;"Pengajuan Ditolak". Untuk lebih jelasnya Anda dapat melihat ini di dalam "tata cara pengajuan".
		</td>
	</tr>
	<tr>
		<td>
			4.	Arahkan pointer pada menu "Pelaporan" maka akan muncul sub - sub menu dari menu pelaporan, Anda dapat melihat menu "Pelaporan Diterima", dan menu &nbsp;&nbsp;&nbsp;&nbsp;"Pengajuan Ditolak". Untuk lebih jelasnya Anda dapat melihat ini di dalam "tata cara pelaporan".
		</td>
	</tr>
	<tr>
		<td>
			5.	Arahkan pointer pada menu "Ubah Informasi" maka akan muncul sub - sub menu dari menu ubah informasi, Anda dapat melihat menu "Ubah Identitas" dan menu &nbsp;&nbsp;&nbsp;&nbsp;"Ubah Kata Sandi".
		</td>
	</tr>
	<tr>
		<td>
			6.	Jika Anda ingin mengubah Informasi tentang Anda, Anda dapat mengklik menu "Ubah Identitas". Di dalam menu tersebut terdapat NIP, Nama, Tanggal Lahir, &nbsp;&nbsp;&nbsp;&nbsp;Telepon, dan Alamat mengenai Anda. Anda hanya dapat mengubah Nama,Tanggal Lahir , Telepon , dan Alamat. Ketika Anda ingin menyimpan perubahannya, &nbsp;&nbsp;&nbsp;&nbsp;Anda dapat mengklik tombol "Simpan" jika membatalkannya Anda dapat mengklik tombol "Batal".
		</td>
	</tr>
	<tr>
		<td>
			7.	Jika Anda ingin mengubah kata sandi Anda , Anda dapat mengklik menu "Ubah Kata Sandi". Di dalam menu tersebut terdapat inputan kata sandi lama Anda, dan &nbsp;&nbsp;&nbsp;&nbsp;kata sandi yang akan diperbaharui. Anda harus mengisi kata sandi lama anda setelah itu mengisi kata sandi baru Anda. Ketika Anda ingin menyimpan &nbsp;&nbsp;&nbsp;&nbsp;perubahannya, Anda dapat mengklik tombol "Simpan" jika membatalkannya Anda dapat mengklik tombol "Batal".
		</td>
	</tr>
	<tr>
		<td>
			8.	Arahkan pointer pada menu "Cetak" maka akan muncul sub - sub menu dari menu cetak, Anda dapat melihat menu "Data Kegiatan", "Data Pengajuan", "Data &nbsp;&nbsp;&nbsp;&nbsp;Pelaporan".
		</td>
	</tr>
	<tr>
		<td>
			9.	Anda dapat mencetak data kegiatan Anda di dalam aplikasi monitoring anggaran ini dengan mengklik Sub menu "Data Kegiatan" yang terdapat di dalam menu &nbsp;&nbsp;&nbsp;&nbsp;"Cetak". Anda dapat memilih waktu tanggal kegiatan Anda dan status kegiatan Anda yang akan Anda Cetak. Setelah itu Anda Klik tombol "Kirim", maka Anda &nbsp;&nbsp;&nbsp;&nbsp;dapat melihat seluruh data kegiatan sesuai dengan pilihan Anda. Jika ingin mencetaknya Anda dapat mengklik tombol "Cetak". .
		</td>
	</tr>
	<tr>
		<td>
			10.	Anda dapat mencetak data pengajuan Anda di dalam aplikasi monitoring anggaran ini dengan mengklik Sub menu "Data pengajuan" yang terdapat di dalam menu &nbsp;&nbsp;&nbsp;&nbsp;"Cetak". Anda dapat memilih waktu tanggal pengajuan Anda dan status pengajuan Anda yang akan Anda Cetak. Setelah itu Anda Klik tombol "Kirim", maka &nbsp;&nbsp;&nbsp;&nbsp;Anda dapat melihat seluruh data pengajuan sesuai dengan pilihan Anda. Jika ingin mencetaknya Anda dapat mengklik tombol "Cetak". .
		</td>
	</tr>
	<tr>
		<td>
			11.	Anda dapat mencetak data pelaporan Anda di dalam aplikasi monitoring anggaran ini dengan mengklik Sub menu "Data pelaporan" yang terdapat di dalam menu &nbsp;&nbsp;&nbsp;&nbsp;"Cetak". Anda dapat memilih waktu tanggal pelaporan Anda dan status pelaporan Anda yang akan Anda Cetak. Setelah itu Anda Klik tombol "Kirim", maka Anda &nbsp;&nbsp;&nbsp;&nbsp;dapat melihat seluruh data pelaporan sesuai dengan pilihan Anda. Jika ingin mencetaknya Anda dapat mengklik tombol "Cetak". .
		</td>
